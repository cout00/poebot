    public static class ItemExtensions {

        public static BaseNames ToBaseName(this string name) {
            var validName = name.Replace(" ", "_").Replace("\'", "__").Replace("-", "___");
            try {
                return (BaseNames)Enum.Parse(typeof(BaseNames), validName);
            }
            catch (Exception) {
                return BaseNames.Debug;
            }
        }

        public static ItemClass ToItemClass(this string name) {
            var validName = name.Replace(" ", "_").Replace("\'", "__").Replace("-", "___");
            try {
                return (ItemClass)Enum.Parse(typeof(ItemClass), validName);
            }
            catch (Exception) {
                return ItemClass.Debug;
            }
        }

        public static string ToText(this BaseNames name) {
            return name.ToString().Replace("_", " ").Replace("__", "\'").Replace("___", "-");
        }

        public static string ToText(this ItemClass name) {
            return name.ToString().Replace("_", " ").Replace("__", "\'").Replace("___", "-");
        }


        public static ItemStatus ToStatus(this string name) {
            try {
                return (ItemStatus)Enum.Parse(typeof(ItemStatus), name);
            }
            catch (Exception) {
                return ItemStatus.Debug;
            }
        }

        public static ItemRarity ToRarity(this string name) {
            try {
                return (ItemRarity)Enum.Parse(typeof(ItemRarity), name);
            }
            catch (Exception) {
                return ItemRarity.Debug;
            }
        }

        public static IEnumerable<string> RemoveEmpty(this IEnumerable<string> self) {
            return self.Where(a => a != string.Empty);
        }

    }

